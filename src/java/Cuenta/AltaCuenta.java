    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        RequestDispatcher vw = request.getRequestDispatcher("/Cuentas/Alta.jsp");
        vw.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String numero_cliente = request.getParameter("numero_cliente");
        String numero_cuenta = request.getParameter("numero_cuenta");
        String tipo_cuenta = request.getParameter("tipo_cuenta");
        int monto = Integer.parseInt(request.getParameter("monto"));
        
        HttpSession session = request.getSession(true);
                
        Cuenta cuenta = new Cuenta(numero_cliente, numero_cuenta, tipo_cuenta, monto);
        session.setAttribute("cuenta", cuenta);
        
        RequestDispatcher view = request.getRequestDispatcher("Cuentas/Detalles.jsp");
        view.forward(request, response);
    }

